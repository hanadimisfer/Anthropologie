from django.shortcuts import render, get_object_or_404, redirect
from django.core.exceptions import ObjectDoesNotExist
from django.contrib.auth.decorators import login_required
from django.contrib.auth.mixins import LoginRequiredMixin
from django.views.generic import ListView, DetailView, View
from .models import Item, Order, OrderItem, ShippingAddress
from .forms import CheckoutForm
from django.utils import timezone
from django.contrib import messages


def product(request):
    context = {
        'items': Item.objects.all()
    }
    return render(request, "products.html", context)


class CheckoutView(View):
    def get(self, *args, **kwargs):
        form = CheckoutForm()
        context = {
            'form': form
        }
        return render(self.request, "checkout.html", context)

    def post(self, *args, **kwargs):
        form = CheckoutForm(self.request.POST or None)
        try:
            order = Order.objects.get(user=self.request.user, ordered=False)
            if form.is_valid():
                street_address = form.cleaned_data.get('street_address')
                apartment_address = form.cleaned_data.get('apartment_address')
                country = form.cleaned_data.get('country')
                zip = form.cleaned_data.get('zip')
                CCname = form.cleaned_data.get('CCname')
                CCnumber = form.cleaned_data.get('CCnumber')
                expiration = form.cleaned_data.get('expiration')
                cvv = form.cleaned_data.get('cvv')
                Shipping_Address = ShippingAddress(
                    user=self.request.user,
                    street_address=street_address,
                    apartment_address=apartment_address,
                    country=country,
                    zip=zip,
                    CCname=CCname,
                    CCnumber=CCnumber,
                    expiration=expiration,
                    cvv=cvv
                )
                Shipping_Address.save()
                order.Shipping_Address = Shipping_Address
                order_items = order.items.all()
                order_items.update(ordered=True)
                for item in order_items:
                    item.save()

                order.ordered = True
                order.save()
                messages.info(
                    self.request, "Congratulations on successfully placing your order! ")
                return redirect('core:home')
            else:
                messages.warning(
                    self.request, "failed, please enter a correct format")
                return redirect('core:checkout')

        except ObjectDoesNotExist:
            messages.warning(self.request, "You do not have an active order")
            return redirect("core:order_summary")


